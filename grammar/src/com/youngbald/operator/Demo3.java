package com.youngbald.operator;

public class Demo3 {
    public static void main(String[] args) {
        //关系运算符返回的结果 正确、错误 只有布尔值
        int a =10;
        int b = 20;
        System.out.println(a>b); //f
        System.out.println(a<b);//t
        System.out.println(a==b);//f
        System.out.println(a!=b);//t
    }
}
